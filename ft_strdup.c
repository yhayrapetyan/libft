#include "libft.h"

char	*ft_strdup(const char *src)
{
	unsigned int	i;
	char			*ptr;

	i = 0;
	while (src[i])
		i++;
	ptr = (char *)malloc((i + 1) * sizeof(*src));
	i = 0;
	if (ptr == NULL)
		return (0);
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
